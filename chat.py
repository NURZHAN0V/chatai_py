# Подключаем инструмент, который умеет отправлять сообщения в интернет
import requests

# Ваш личный пароль от OpenRouter — храните его в секрете
KEY = "ваш ключ"

# Повторяем действия снова и снова, пока программа работает
while True:
    # Ждём, когда вы напишете сообщение, и сохраняем его в переменную
    value = input("Ты: ")

    # Собираем «посылку» для нейросети: указываем модель и ваш вопрос
    post = {
        "model": "z-ai/glm-4.5-air:free",  # Какая нейросеть будет отвечать
        "messages": [
            # Сам вопрос: роль "user" означает, что пишет человек
            {"role": "user", "content": value}
        ]
    }

    # Отправляем посылку на сервер OpenRouter и ждём ответ
    response = requests.post(
        "https://openrouter.ai/api/v1/chat/completions",  # Адрес, куда отправляем
        headers={"Authorization": f"Bearer {KEY}"},       # Прикладываем ваш ключ-пропуск
        json=post                                          # Сама посылка с вопросом
    )

    # Проверяем, всё ли прошло хорошо (код 200 = успех)
    if response.status_code == 200:
        # Достаем из ответа только текст, который написала нейросеть
        message = response.json()["choices"][0]["message"]["content"]
        # Показываем ответ на экране
        print("Нейросеть:", message)
    else:
        # Если что-то пошло не так — сообщаем об этом
        print("Что-то пошло не так 😮")