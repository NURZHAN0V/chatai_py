# Перед запуском открой консоль и напиши: pip install requests

import requests

# Вставь сюда свой личный пароль от сайта OpenRouter
КЛЮЧ = "ваш ключ"

# Повторяем действия снова и снова, пока программа работает
while True:
    # Ждём, когда человек напишет своё сообщение
    вопрос = input("Ты: ")

    # Если человек написал «выход», прощаемся и останавливаем программу
    if вопрос.lower() == "выход":
        print("Пока!")
        break

    # Собираем вопрос в удобную форму, указывая имя помощника и сам текст
    посылка = {
        "model": "z-ai/glm-4.5-air:free",
        "messages": [{"role": "user", "content": вопрос}]
    }

    # Отправляем посылку через интернет в OpenRouter и ждём ответ
    ответ = requests.post(
        "https://openrouter.ai/api/v1/chat/completions",
        headers={"Authorization": f"Bearer {КЛЮЧ}"},
        json=посылка
    )

    # Достаём из полученного ответа только слова, которые придумала нейросеть
    слова_нейросети = ответ.json()["choices"][0]["message"]["content"]

    # Выводим эти слова на экран
    print("Нейросеть:", слова_нейросети)